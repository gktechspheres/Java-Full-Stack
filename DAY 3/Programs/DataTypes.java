class DataTypes {
    public static void main(String[] args) {
        // 1 byte Byte
        byte b = 10;
        System.out.println("Value of x: " + b);

        // 2 bytes Short
        short s = 10;
        System.out.println("Value of y: " + s);

        // 4 bytes Integer
        int i = 10; 
        System.out.println("Value of a: " + i);

        // 8 bytes Long
        long l = 10;
        System.out.println("Value of z: " + l);

        // 4 bytes Floating point
        float f = 10.5f; 
        System.out.println("Value of b: " + f);

        // 8 bytes Floating point
        double d = 10.5; 
        System.out.println("Value of c: " + d);

        // 2 bytes Character
        char c = 'A'; 
        System.out.println("Value of d: " + c);

        // 1 bit Boolean
        boolean bl = true; 
        System.out.println("Value of e: " + bl);

        // String
        String st = "Hello"; 
        System.out.println("Value of f: " + st);
        
        // Array
        int arr[] = {1, 2, 3, 4, 5}; 
        System.out.println("Value of arr: " + arr);
    }
}